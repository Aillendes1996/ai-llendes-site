/* set / persist language */
function setLang(lang) {
  const dict = I18N[lang] || I18N.en;
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) el.innerHTML = dict[key]; // allow <60s html
  });
  document.getElementById("lang-en")?.setAttribute("aria-pressed", lang === "en");
  document.getElementById("lang-fr")?.setAttribute("aria-pressed", lang === "fr");
  localStorage.setItem("lang", lang);
}
document.getElementById("lang-en")?.addEventListener("click", () => setLang("en"));
document.getElementById("lang-fr")?.addEventListener("click", () => setLang("fr"));
setLang(localStorage.getItem("lang") || "en");

/* smooth scroll for [data-scroll] links */
document.querySelectorAll("[data-scroll]").forEach(el=>{
  el.addEventListener("click",e=>{
    e.preventDefault();
    const id = el.getAttribute("data-scroll");
    const target = document.querySelector(id);
    if(target) target.scrollIntoView({behavior:"smooth", block:"start"});
  });
});

/* simple accordion */
document.querySelectorAll(".accordion-item .accordion-trigger").forEach(btn=>{
  btn.addEventListener("click", ()=>{
    const item = btn.closest(".accordion-item");
    const expanded = btn.getAttribute("aria-expanded") === "true";
    btn.setAttribute("aria-expanded", String(!expanded));
    item.setAttribute("aria-expanded", String(!expanded));
  });
});

